/// -----------------------------------------------
/// アプリケーション全体で使用するTalkerインスタンス
///
/// 以下の設定で初期化:
/// * タイムスタンプ形式: 年月日と時刻
/// * 最大履歴保持数: 200件
///
/// このインスタンスを使用して以下の操作が可能:
/// * ログの記録
/// * エラーの記録
/// * デバッグ情報の出力
/// * ログ履歴の管理
/// -----------------------------------------------
final talker = TalkerFlutter.init(
  settings: TalkerSettings(
    timeFormat: TimeFormat.yearMonthDayAndTime,
    maxHistoryItems: 200,
  ),
);
